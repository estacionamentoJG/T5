    public Double trata (String texto) {
        String stringFinal = "";
        
        for (int i=0; i<texto.length(); i++) {
            if (i > 2)
                stringFinal += Character.toString(texto.charAt(i));
         }

        return Double.parseDouble(stringFinal);
        
    }
    
